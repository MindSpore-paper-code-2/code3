class SwinPrompt(SwinTransformer):
    def __init__(self,
                 freeze_backbone=True,
                 pretrain_img_size=224,
                 in_channels=3,
                 embed_dims=96,
                 patch_size=4,
                 window_size=7,
                 mlp_ratio=4,
                 depths=(2, 2, 6, 2),
                 num_heads=(3, 6, 12, 24),
                 strides=(4, 2, 2, 2),
                 out_indices=(0, 1, 2, 3),
                 qkv_bias=True,
                 qk_scale=None,
                 patch_norm=True,
                 drop_rate=0.,
                 attn_drop_rate=0.,
                 drop_path_rate=0.1,
                 use_abs_pos_embed=False,
                 act_cfg=None,
                 norm_cfg=None,
                 with_cp=False,
                 conv_inplane=64,
                 n_points=4,
                 deform_num_heads=16,
                 init_values=1e-6,
                 with_cffn=True,
                 cffn_ratio=0.25,
                 deform_ratio=0.5,
                 norm_layer=None,
                 use_extra_extractor=False,
                 pretrained=None,
                 frozen_stages=-1):
        super().__init__(
                 pretrain_img_size=pretrain_img_size, in_channels=in_channels,
                 embed_dims=embed_dims, patch_size=patch_size,
                 window_size=window_size, mlp_ratio=mlp_ratio,
                 depths=depths, num_heads=num_heads, strides=strides,
                 out_indices=out_indices, qkv_bias=qkv_bias,
                 qk_scale=qk_scale, patch_norm=patch_norm, drop_rate=drop_rate,
                 attn_drop_rate=attn_drop_rate, drop_path_rate=drop_path_rate,
                 use_abs_pos_embed=use_abs_pos_embed,
                 act_cfg=act_cfg, norm_cfg=norm_cfg, with_cp=with_cp,
                 pretrained=None, frozen_stages=frozen_stages)

        if act_cfg is None:
            act_cfg = dict(type='GELU')
        if norm_cfg is None:
            norm_cfg = dict(type='LN')

        norm_layer = norm_layer or partial(nn.LayerNorm, eps=1e-6)
        self.next_dim = [256, 512, 1024, None]
        self.spm = SpatialPriorModule(inplanes=conv_inplane, embed_dim=embed_dims, with_cp=False)

        self.interactions = nn.Sequential(*[
            InteractionBlockPrompt(dim=embed_dims*2**i, num_heads=deform_num_heads, n_points=n_points,
                             init_values=init_values, drop_path=drop_path_rate,
                             norm_layer=norm_layer, with_cffn=with_cffn,
                             cffn_ratio=cffn_ratio, deform_ratio=deform_ratio,
                             extra_extractor=use_extra_extractor,
                             with_cp=with_cp, next_dim=self.next_dim[i])
            for i in range(len(self.stages))
        ])

        self.level_embed = nn.Parameter(torch.zeros(3, embed_dims))

    def _add_level_embed(self, c1, c2, c3):
        c1 = c1 + self.level_embed[0]
        c2 = c2 + self.level_embed[1]
        c3 = c3 + self.level_embed[2]
        return c1, c2, c3

    def forward(self, x):
        # SPM forward
        c1, c2, c3 = self.spm(x) # b*65536*128 b*16384*128 b*4096*128 b*1024*128
        c1, c2, c3 = self._add_level_embed(c1, c2, c3)
        x, hw_shape = self.patch_embed(x) # b*21504*128
        if self.use_abs_pos_embed:
            x = x + self.absolute_pos_embed
        x = self.drop_after_pos(x)

        # Interaction with spm prompt
        outs = []
        for i, (layer, stage) in enumerate(zip(self.interactions, self.stages)):

            _, c1, c2, c3, x, hw_shape, out, out_hw_shape = layer(x, c1, c2, c3, stage, hw_shape, backbone='swin')
            if i in self.out_indices:
                norm_layer = getattr(self, f'norm{i}')
                out = norm_layer(out)
                out = out.view(-1, *out_hw_shape, self.num_features[i]).permute(0, 3, 1, 2).contiguous()
                outs.append(out)
        return outs
